function add(a, b) {
  add = a + b;
  return(add);
}

function substract(a, b) {
  substract = a - b;
  return(substract);
}

function multiply(a, b) {
  multiply = a * b;
  return(multiply)
}

function divide(a, b) {
  divide = a/b;
  return(divide);
}

function inc(n) {
  inc = ++n;
  return(inc)
}